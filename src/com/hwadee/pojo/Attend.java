package com.hwadee.pojo;


public class Attend {
	private String attend_id;

	private String attend_time;
	private String total_number;
	private String absent_number;
	private String leave_number;
	private String file_name;
	private String submission_date;


	public String getAttend_id() {
		return attend_id;
	}
	public void setAttend_id(String attend_id) {
		this.attend_id = attend_id;
	}
	public String getAttend_time() {
		return attend_time;
	}
	public void setAttend_time(String attend_time) {
		this.attend_time = attend_time;
	}
	public String getTotal_number() {
		return total_number;
	}
	public void setTotal_number(String total_number) {
		this.total_number = total_number;
	}
	public String getAbsent_number() {
		return absent_number;
	}
	public void setAbsent_number(String absent_number) {
		this.absent_number = absent_number;
	}
	public String getLeave_number() {
		return leave_number;
	}
	public void setLeave_number(String leave_number) {
		this.leave_number = leave_number;
	}
	public String getFile_name() {
		return file_name;
	}
	public void setFile_name(String file_name) {
		this.file_name = file_name;
	}
	public String getSubmission_date() {
		return submission_date;
	}
	public void setSubmission_date(String submission_date) {
		this.submission_date = submission_date;
	}
	
	
}
